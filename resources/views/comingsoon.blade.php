    <script type="text/javascript">
        $("#subscribe").validate({
        rules: {
            user_type: 'required',
            first_name: 'required',
            last_name: 'required',
            user_email: 'required',
            user_mobile: {
                required: true,
                minlength: 10,
                maxlength: 10,
                number: true,
            },
        },
        messages: {
          user_type: "Please select an option ",
          first_name: "Please enter valid first name",
          last_name: "Please enter valid last name",
          user_email: "Please enter a valid email",
          user_mobile :  "Please enter a valid mobile number "          
        },
        submitHandler: function(form) {
            var formData = $("form#subscribe").serialize();
            $.ajax({
                url: '/account/subscribe',
                type: 'POST',
                data: formData,
                dataType: "json",
                success: function (data) {
                    $('p#LoadingImag').html('');
                    if(data.status == 'success'){
                        $('#resonse-txt').html(data.msg)
                        $('#subscribe').trigger("reset");
                    }else{
                        $('#resonse-txt').html(data.msg)
                    }                    
                }
            });
        },
    });        
